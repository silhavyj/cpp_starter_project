#pragma once

namespace MyMath
{
    /// Adds up two given integer numbers
    /// \param x the first number
    /// \param y the second number
    /// \return x + y
    int add(int x, int y);

    /// Subtracts two given integer numbers
    /// \param x the first number
    /// \param y the second number
    /// \return x - y
    int sub(int x, int y);

    /// Multiplies two given integer numbers
    /// \param x the first number
    /// \param y the second number
    /// \return x * y
    int mult(int x, int y);
}